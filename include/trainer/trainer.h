///////////////////////////////////////////////////////////////////////////////
//
// Defines the base trainer class.
//
///////////////////////////////////////////////////////////////////////////////

#ifndef MININET_TRAINER_TRAINER_H
#define MININET_TRAINER_TRAINER_H

#include <util/types.h>
#include <net/network.h>
#include <dataset/dataset.h>

namespace mininet {

class Trainer {
public:
  virtual ~Trainer() {}
  explicit Trainer(const Network& network, const Dataset& dataset)
    : network_(network),
      dataset_(dataset) {}

  // All trainers must implement this interface.
  virtual void Train() = 0;
  virtual double Test() const = 0;

protected:
  Network network_;
  Dataset dataset_;
}; // class Trainer

} // namespace mininet

#endif
